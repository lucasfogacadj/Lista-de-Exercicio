
public class Atividade3 {
	
		public static void main(String[] args) {
			
			int y=5;
			int z=5;
			
			System.out.println("For");
			
			for(int x=5; x<11; x++){
				System.out.println(x);
			}
			
			System.out.println("");
			System.out.println("Do/while");
			do{
				System.out.println(y);
				y++;
			}while(y<11);
			
			System.out.println("");
			System.out.println("While");
			
			while(z<11){
				System.out.println(z);
				z++;
			}
			
		}

}
